# Ask for user input and convert to integers
number1 = int(input("Enter the first integer: "))
number2 = int(input("Enter the second integer: "))
number3 = int(input("Enter the third integer: "))

# Perform calculations
sum_of_numbers = number1 + number2 + number3
subtraction_result = number1 - number2
multiplication_result = number3 * number1
division_result = sum_of_numbers / number3

# Print results
print(f"Sum of all numbers: {sum_of_numbers}") 
print("First number minus second number:", subtraction_result)
print("Third number multiplied by first number:", multiplication_result)
print("Sum of all three numbers divided by the third number:", division_result)


